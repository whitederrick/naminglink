create table if not exists public.official_hanja_sources (
  id uuid primary key default gen_random_uuid(),
  source_key text not null unique,
  title text not null,
  publisher text,
  rule_reference text,
  version_label text,
  effective_date date,
  source_url text,
  source_file_name text,
  source_file_path text,
  source_sha256 text,
  status text not null default 'draft' check (
    status in ('draft', 'reviewed', 'production', 'archived')
  ),
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.official_hanja_rules (
  id uuid primary key default gen_random_uuid(),
  source_id uuid not null references public.official_hanja_sources(id) on delete cascade,
  rule_code text not null,
  description text not null,
  is_active boolean not null default true,
  sort_order integer not null default 0,
  created_at timestamptz not null default now(),
  unique (source_id, rule_code)
);

create table if not exists public.official_hanja_entries (
  id uuid primary key default gen_random_uuid(),
  source_id uuid not null references public.official_hanja_sources(id) on delete cascade,
  hangul_syllable text not null,
  hanja text not null,
  designated_reading text not null,
  meaning_ko text,
  table_section text not null default 'official_table',
  page_number integer,
  stroke_count integer,
  radical text,
  is_name_usable boolean not null default true,
  review_status text not null default 'ocr' check (
    review_status in ('draft', 'ocr', 'reviewed', 'production', 'rejected', 'sample')
  ),
  notes text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (source_id, hangul_syllable, hanja, designated_reading, table_section)
);

create table if not exists public.official_hanja_variants (
  id uuid primary key default gen_random_uuid(),
  source_id uuid not null references public.official_hanja_sources(id) on delete cascade,
  base_hanja text not null,
  variant_hanja text not null,
  variant_type text not null default 'other' check (
    variant_type in ('same_character', 'popular_form', 'abbreviation', 'radical', 'other')
  ),
  rule_code text,
  notes text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  unique (source_id, base_hanja, variant_hanja, variant_type)
);

create table if not exists public.official_pronunciation_sources (
  id uuid primary key default gen_random_uuid(),
  source_key text not null unique,
  title text not null,
  publisher text,
  source_url text,
  source_file_name text,
  source_file_path text,
  source_sha256 text,
  page_count integer,
  extraction_status text not null default 'pending' check (
    extraction_status in ('pending', 'needs_ocr', 'extracted', 'reviewed', 'production', 'archived')
  ),
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.official_pronunciation_chunks (
  id bigint generated by default as identity primary key,
  source_id uuid not null references public.official_pronunciation_sources(id) on delete cascade,
  page_number integer not null,
  section_title text,
  content text not null,
  review_status text not null default 'ocr' check (
    review_status in ('ocr', 'reviewed', 'production', 'rejected')
  ),
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.official_pronunciation_entries (
  id uuid primary key default gen_random_uuid(),
  source_id uuid not null references public.official_pronunciation_sources(id) on delete cascade,
  hangul text not null,
  romanization text not null,
  surname_usable boolean,
  given_name_usable boolean,
  page_number integer,
  review_status text not null default 'ocr' check (
    review_status in ('ocr', 'reviewed', 'production', 'rejected')
  ),
  notes text,
  metadata jsonb not null default '{}'::jsonb,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (source_id, hangul, romanization)
);

