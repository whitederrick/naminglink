drop policy if exists "Anyone can read production pronunciation entries" on public.official_pronunciation_entries;
create policy "Anyone can read production pronunciation entries"
  on public.official_pronunciation_entries for select
  using (
    review_status = 'production' and exists (
      select 1 from public.official_pronunciation_sources sources
      where sources.id = official_pronunciation_entries.source_id
        and sources.extraction_status = 'production'
    )
  );

drop policy if exists "Anyone can read production Hanja sources" on public.official_hanja_sources;
create policy "Anyone can read production Hanja sources"
  on public.official_hanja_sources for select
  using (status = 'production');

drop policy if exists "Anyone can read active production Hanja rules" on public.official_hanja_rules;
create policy "Anyone can read active production Hanja rules"
  on public.official_hanja_rules for select
  using (
    is_active = true and exists (
      select 1 from public.official_hanja_sources sources
      where sources.id = official_hanja_rules.source_id
        and sources.status = 'production'
    )
  );

drop policy if exists "Anyone can read production Hanja entries" on public.official_hanja_entries;
create policy "Anyone can read production Hanja entries"
  on public.official_hanja_entries for select
  using (
    review_status = 'production' and is_name_usable = true and exists (
      select 1 from public.official_hanja_sources sources
      where sources.id = official_hanja_entries.source_id
        and sources.status = 'production'
    )
  );

drop policy if exists "Anyone can read production Hanja variants" on public.official_hanja_variants;
create policy "Anyone can read production Hanja variants"
  on public.official_hanja_variants for select
  using (
    exists (
      select 1 from public.official_hanja_sources sources
      where sources.id = official_hanja_variants.source_id
        and sources.status = 'production'
    )
  );

